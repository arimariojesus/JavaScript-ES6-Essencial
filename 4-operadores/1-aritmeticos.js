// Móduulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

12 % 5 // retorna 2


// Incremento (++) e Decremento (--)
++x
x++
--x
x--

const a = ++2 // a == 3
const b = 2++ // b == 2

// Negação (-) e Adição (+)
-3      // 'Negativa' o 3
+"3"    // retorna 3 (+ converte string para numérico)
+true   // retorna 1
+false  // retorna 0
-true   // retorna -1

// Operador de exponenciação (**)
2 ** 3     // retorna 8
10 ** -1   // retorna 0.1

//Operador de agrupamento ()
2 * (3 + 2)